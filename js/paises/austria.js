var indiceAustria = 1;
var pantallaAustria = document.getElementById('ver-austria');
var imgAustria = document.createElement("img");
var iframeAustria = document.createElement("iframe");

//-- Ctes.
const totalAustria = 256;

function mostrarAustria(auxAustria) {
    
    //-- Limpiar la pantalla.
    pantallaAustria.innerHTML = "";

    //-- Verificar si es una imagen o un vídeo.
    if(imagenesIDaustria[auxAustria-1]) {
        imgAustria.src = imagenesIDaustria[auxAustria-1];
        pantallaAustria.appendChild(imgAustria);
    }else {
        iframeAustria.src =  videosIDaustria[auxAustria-1];
        iframeAustria.width = "400";
        iframeAustria.height = "533";
        iframeAustria.setAttribute("allow", "autoplay");
        iframeAustria.setAttribute("allowFullscreen", "false");
        pantallaAustria.appendChild(iframeAustria);
    }
}

function imgAnteriorAustria() {
    if(indiceAustria > 1) {
        indiceAustria--;
    }else {
        indiceAustria = totalAustria;
    }
    mostrarAustria(indiceAustria);
}

function imgSiguienteAustria() {
    if(indiceAustria < totalAustria) {
        indiceAustria++;
    }else {
        indiceAustria = 1;
    }
    mostrarAustria(indiceAustria);
}

//-- Pulsar flecha izquierda.
document.getElementById("izq-austria").addEventListener("click", imgAnteriorAustria);
//-- Pulsar flecha derecha.
document.getElementById("der-austria").addEventListener("click", imgSiguienteAustria);

//-- Punto de inicio del programa.
mostrarAustria(indiceAustria);