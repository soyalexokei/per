var indiceUSA = 1;
var pantallaUSA = document.getElementById('ver-usa');
var imgUSA = document.createElement("img");
var iframeUSA = document.createElement("iframe");

//-- Ctes.
const totalUSA = 223;

function mostrarUSA(auxUSA) {
    
    //-- Limpiar la pantalla.
    pantallaUSA.innerHTML = "";

    //-- Verificar si es una imagen o un vÃ­deo.
    if(imagenesIDusa[auxUSA-1]) {
        imgUSA.src = imagenesIDusa[auxUSA-1];
        pantallaUSA.appendChild(imgUSA);
    }else {
        iframeUSA.src =  videosIDusa[auxUSA-1];
        iframeUSA.width = "400";
        iframeUSA.height = "533";
        iframeUSA.setAttribute("allow", "autoplay");
        iframeUSA.setAttribute("allowFullscreen", "false");
        pantallaUSA.appendChild(iframeUSA);
    }
}

function imgAnteriorUSA() {
    if(indiceUSA > 1) {
        indiceUSA--;
    }else {
        indiceUSA = totalUSA;
    }
    mostrarUSA(indiceUSA);
}

function imgSiguienteUSA() {
    if(indiceUSA < totalUSA) {
        indiceUSA++;
    }else {
        indiceUSA = 1;
    }
    mostrarUSA(indiceUSA);
}

//-- Pulsar flecha izquierda.
document.getElementById("izq-usa").addEventListener("click", imgAnteriorUSA);
//-- Pulsar flecha derecha.
document.getElementById("der-usa").addEventListener("click", imgSiguienteUSA);

//-- Punto de inicio del programa.
mostrarUSA(indiceUSA);