var indicePortugal = 1;
var pantallaPortugal = document.getElementById('ver-portugal');
var imgPortugal = document.createElement("img");
var iframePortugal = document.createElement("iframe");

//-- Ctes.
const totalPortugal = 119;

function mostrarPortugal(auxPortugal) {
    
    //-- Limpiar la pantalla.
    pantallaPortugal.innerHTML = "";

    //-- Verificar si es una imagen o un vÃ­deo.
    if(imagenesIDportugal[auxPortugal-1]) {
        imgPortugal.src = imagenesIDportugal[auxPortugal-1];
        pantallaPortugal.appendChild(imgPortugal);
    }else {
        iframePortugal.src = videosIDportugal[auxPortugal-1];
        iframePortugal.width = "400";
        iframePortugal.height = "533";
        iframePortugal.setAttribute("allow", "autoplay");
        iframePortugal.setAttribute("allowFullscreen", "false");
        pantallaPortugal.appendChild(iframePortugal);
    }
}

function imgAnteriorPortugal() {
    if(indicePortugal > 1) {
        indicePortugal--;
    }else {
        indicePortugal = totalPortugal;
    }
    mostrarPortugal(indicePortugal);
}

function imgSiguientePortugal() {
    if(indicePortugal < totalPortugal) {
        indicePortugal++;
    }else {
        indicePortugal = 1;
    }
    mostrarPortugal(indicePortugal);
}

//-- Pulsar flecha izquierda.
document.getElementById("izq-portugal").addEventListener("click", imgAnteriorPortugal);
//-- Pulsar flecha derecha.
document.getElementById("der-portugal").addEventListener("click", imgSiguientePortugal);

//-- Punto de inicio del programa.
mostrarPortugal(indicePortugal);