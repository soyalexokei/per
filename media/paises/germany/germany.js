var imgGermany = document.createElement("img");
var iframeGermany = document.createElement("iframe");

//-- Ctes.
const totalGermany = 300;

function mostrarGermany(auxGermany) {
    
    //-- Limpiar la pantalla.
    pantallaGermany.innerHTML = "";

    //-- Verificar si es una imagen o un vídeo.
    if(imagenesIDgermany[auxGermany-1]) {
        imgGermany.src = imagenesIDgermany[auxGermany-1];
        pantallaGermany.appendChild(imgGermany);
    }else {
        iframeGermany.src =  videosIDgermany[auxGermany-1];
        iframeGermany.width = "400";
        iframeGermany.height = "533";
        iframeGermany.setAttribute("allow", "autoplay");
        iframeGermany.setAttribute("allowFullscreen", "false");
        pantallaGermany.appendChild(iframeGermany);
    }
}

function imgAnteriorGermany() {
    if(indiceGermany > 1) {
        indiceGermany--;
    }else {
        indiceGermany = totalGermany;
    }
    mostrarGermany(indiceGermany);
}

function imgSiguienteGermany() {
    if(indiceGermany < totalGermany) {
        indiceGermany++;
    }else {
        indiceGermany = 1;
    }
    mostrarGermany(indiceGermany);
}

//-- Pulsar flecha izquierda.
document.getElementById("izq-germany").addEventListener("click", imgAnteriorGermany);
//-- Pulsar flecha derecha.
document.getElementById("der-germany").addEventListener("click", imgSiguienteGermany);

