var indiceKosovo = 1;
var pantallaKosovo = document.getElementById('ver-kosovo');
var imgKosovo = document.createElement("img");
var iframeKosovo = document.createElement("iframe");

//-- Ctes.
const totalKosovo = 97;

function mostrarKosovo(auxKosovo) {
    
    //-- Limpiar la pantalla.
    pantallaKosovo.innerHTML = "";

    //-- Verificar si es una imagen o un vÃ­deo.
    if(imagenesIDkosovo[auxKosovo-1]) {
        imgKosovo.src = imagenesIDkosovo[auxKosovo-1];
        pantallaKosovo.appendChild(imgKosovo);
    }else {
        iframeKosovo.src = videosIDkosovo[auxKosovo-1];
        iframeKosovo.width = "400";
        iframeKosovo.height = "533";
        iframeKosovo.setAttribute("allow", "autoplay");
        iframeKosovo.setAttribute("allowFullscreen", "false");
        pantallaKosovo.appendChild(iframeKosovo);
    }
}

function imgAnteriorKosovo() {
    if(indiceKosovo > 1) {
        indiceKosovo--;
    }else {
        indiceKosovo = totalKosovo;
    }
    mostrarKosovo(indiceKosovo);
}

function imgSiguienteKosovo() {
    if(indiceKosovo < totalKosovo) {
        indiceKosovo++;
    }else {
        indiceKosovo = 1;
    }
    mostrarKosovo(indiceKosovo);
}

//-- Pulsar flecha izquierda.
document.getElementById("izq-kosovo").addEventListener("click", imgAnteriorKosovo);
//-- Pulsar flecha derecha.
document.getElementById("der-kosovo").addEventListener("click", imgSiguienteKosovo);

//-- Punto de inicio del programa.
mostrarKosovo(indiceKosovo);